    private void RegisterBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_RegisterBtnActionPerformed
        if(!checkEmailExist(regEmail.getText()) && registrate(regUsername.getText(), regEmail.getText(), regPassword.getText())){
            ErrorText.setText("Succes Registration! An Email has been send");
            welcomeMail(regEmail.getText());// ----> ACTIVATE WHEN DONE
        }else{     
            ErrorText.setText("Username Already exist! Try another");
            if (valUsername(regUsername.getText()) == false) {
                ErrorText.setText("Username does not fulfill the requirements!!");
            }else if (checkEmailExist(regEmail.getText())) {
                ErrorText.setText("Email already exist in database");
            }else if (valEmail(regEmail.getText())== false) {
                ErrorText.setText("Email does not fulfill the requirements!!");
            }else if (valPassword(regPassword.getText())== false) {
                ErrorText.setText("Password does not fulfill the requirements!!");
            }
        }
    }//GEN-LAST:event_RegisterBtnActionPerformed

