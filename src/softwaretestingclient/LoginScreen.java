    private void SendPasswordBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SendPasswordBtnActionPerformed
        if (valEmail(ForgotEmail.getText()) == false) {
            ErrorText.setText("Not a correct email try again.");
        }else{
            
            String newPW = passwordGen();
            
            if (changePassword(ForgotEmail.getText() , newPW)==false) {
                ErrorText.setText("Email not found!");
            }else{
                sendPassword(ForgotEmail.getText(), newPW);
                ErrorText.setText("Password send to Email!");
            }
            
        }
    }//GEN-LAST:event_SendPasswordBtnActionPerformed

